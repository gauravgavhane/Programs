#include<stdio.h>

////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Pattern
//  Description :   Used to print the pattern
//  Input :         Integer
//  Output :        Void
//
////////////////////////////////////////////////////////////////////////////

void Pattern(int iNo)
{
    int iCnt = 0, iMult = 0;

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        iMult = iMult + 2;
        
        printf("%d\t",iMult);
    }
    printf("\n");
}

////////////////////////////////////////////////////////////////////////////
// Accept number from user and display below pattern
////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number of elements : ");
    scanf("%d",&iValue);

    Pattern(iValue);

    return 0;
}

