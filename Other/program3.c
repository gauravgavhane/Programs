#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CircleArea
//  Description :   Used to calculate area of a circle
//  Input :         float
//  Output :        double
//
//////////////////////////////////////////////////////////////////////////////////////

double CircleArea(float fRadius)
{
    float PI = 3.14;
    double dArea = 0.0;

    dArea = PI * fRadius * fRadius;

    return dArea;

}

//////////////////////////////////////////////////////////////////////////////////////
// Write a program which accept radius circle from user & calculate its area.
// Consider value of PI as 3.14 (Area = PI * radius * radius)
//////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0.0;
    double dRet = 0.0;

    printf("Enter radius : ");
    scanf("%f",&fValue);
    
    dRet = CircleArea(fValue);

    printf("Area of circle is %lf\n",dRet);

    return 0;
}

