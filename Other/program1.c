int DollarToINR(int iNo)
{
    int iRupees = 0;

    iRupees = iNo * 70;

    return iRupees;
}

//////////////////////////////////////////////////////////////////////////////////////
// Accept amount in US Dollar and return it correspomding value in Indian Currency
// Consider 1$ as 70 rupees.
//////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;

    printf("Enter number of USD : ");
    scanf("%d",&iValue);

    iRet = DollarToINR(iValue);

    printf("Value in INR is %d\n",iRet);
    
    return 0;
}

