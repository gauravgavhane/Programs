#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : KMtoMeter
//  Description :   Used to convert KM to meter
//  Input :         Integer
//  Output :        Integer
//
//////////////////////////////////////////////////////////////////////////////////////

int KMtoMeter(int iNo)
{
    int iAns = 0;

    iAns = iNo * 1000;

    return iAns;

}

//////////////////////////////////////////////////////////////////////////////////////
// Write a program which accept distance in kilometre and convert it into meter
//////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0, iRet = 0;

    printf("Enter distance : ");
    scanf("%d",&iValue);

    iRet = KMtoMeter(iValue);

    printf("%d KM is equal to %d meters \n",iValue,iRet);

    return 0;
}

