#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : FhtoCs
//  Description :   Used to convert Fahrenheit to celcius
//  Input :         float
//  Output :        double
//
//////////////////////////////////////////////////////////////////////////////////////

double FhtoCs(float fTemp)
{
    double dAns = 0.0;

    dAns = (fTemp - 32) * (5.0 / 9.0);
    
    return dAns;

}

//////////////////////////////////////////////////////////////////////////////////////
// Write a program which accept temperature in Fahrenheit and convert it into celsius.
// 1 celsius = (Fahrenheit - 32) * (5/9)
//////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0.0;
    double dRet = 0.0;

    printf("Enter temperature in Fahrenheit : ");
    scanf("%f",&fValue);

    dRet = FhtoCs(fValue);

    printf("Conversion of Fahrenheit to celsius is %lf\n",dRet);

    return 0;
}

