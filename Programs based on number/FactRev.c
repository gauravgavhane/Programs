#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  FactRev
//  Description :   Used to perform display factors in a reverse order
//  Input :         Integer
//  Output :        Void
//
/////////////////////////////////////////////////////////////////////////////////////////////

void FactRev(int iNo)
{
    int iCnt = 0;

    if(iNo < 0)
    {
        iNo = -iNo;
    }

    printf("Factors in reverse order : \n");
    
    for(iCnt = (iNo/2); iCnt >= 1; iCnt--)
    {
        if((iNo % iCnt) == 0)
        {
            printf("%d\t",iCnt);
        }
    }
    printf("\n");
}

/////////////////////////////////////////////////////////////////////////////////////////////
// Write a program which accept number from user and display its factors in decreasing order
/////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    FactRev(iValue);
    
    return 0;
}

