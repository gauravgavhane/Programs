#include<stdio.h>

void Display(int iNo)
{
    int iCnt = 0;

    for(iCnt = iNo; iCnt >= 1; iCnt--)
    {
        printf("%d\t",iCnt);       
    }
}

/////////////////////////////////////////////////////////////////////////
//  Accept number from user and display the pattern
/////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue =0;

    printf("Enter ter the value :\n");
    scanf("%d",&iValue);

    Display(iValue);   

    return 0;
}

