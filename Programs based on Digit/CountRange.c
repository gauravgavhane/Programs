#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CountRange
//  Description :   Used to count the range between digits
//  Input :         Integer
//  Output :        Integer
//
//////////////////////////////////////////////////////////////////////////////////////

int CountRange(int iNo)
{
    int iDigit = 0, iCnt = 0;

    while (iNo != 0)
    {
        iDigit = iNo % 10;
        iNo = iNo / 10;

        if ((iDigit > 3) && (iDigit < 7))
        {
            iCnt++;
        }
    }
    return iCnt;
}

//////////////////////////////////////////////////////////////////////////////////////
// Accept number from user and return the count of digits in between 3 and 7
//////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;

    printf("Enter the number : ");
    scanf("%d",&iValue);

    iRet = CountRange(iValue);

    printf("Numbers of digits between range : %d\n",iRet);

    return 0;
}

