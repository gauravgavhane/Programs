#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  SumNonFact
//  Description :   Used to perform summation of all its non-factors
//  Input :         Integer
//  Output :        Integer
//
/////////////////////////////////////////////////////////////////////////////////////////////

int SumNonFact(int iNo)
{
    int iCnt = 0, iSum = 0;

    if(iNo < 0)
    {
        iNo = -iNo;
    }

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        if((iNo % iCnt) != 0)
        {
            iSum = iSum + iCnt;
        }
    }
    return iSum;
}

/////////////////////////////////////////////////////////////////////////////////////////////
// Write a program which accept number from user and return summation of all its non-factors
/////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    iRet = SumNonFact(iValue);
    printf("Summation of non factors is %d\n",iRet);
    
    return 0;
}

